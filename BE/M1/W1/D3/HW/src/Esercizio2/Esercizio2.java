package Esercizio2;

public class Esercizio2 {
	
	public static void intToString(int n) {
		switch (n) {
		case (0):
			System.out.println("zero");
			break;

		case (1):
			System.out.println("uno");
			break;

		case (2):
			System.out.println("due");
			break;

		case (3):
			System.out.println("tre");
			break;

		default:
			System.out.println("Qualsiasi altro numero");
			break;
		}
	}

}
